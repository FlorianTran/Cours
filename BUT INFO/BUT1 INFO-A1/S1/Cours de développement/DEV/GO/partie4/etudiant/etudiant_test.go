package etudiant

import (
	"testing"
)

//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//* Liste des etudiants
//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

var e0 Etudiant = Etudiant{Nom: "Jackson", Prenom: "Mike", Notes: []float32{10, 15}}
var eVide Etudiant = Etudiant{Nom: "vide", Prenom: "vide", Notes: []float32{}}

//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//* Test Vide etudiant
//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

func TestVide(t *testing.T) {
	if eVide.Moyenne() != 0 {
		t.Fail()
	}
}

//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//* Test etudiant
//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Test 00 moyenne des notes de l'étudiant
func Test00(t *testing.T) {
	if e0.Moyenne() != 12.5 {
		t.Fail()
	}
}

// Test 00 moyenne des notes de l'étudiant après l'ajout d'une note
func Test01(t *testing.T) {
	e0.AddNote(20)
	if e0.Moyenne() != 15 {
		t.Fail()
	}
}
